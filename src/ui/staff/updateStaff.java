    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        Object[] options = {"Yes","No"};
        JCheckBox[] checkBox={jCheckBox1,jCheckBox2,jCheckBox3,jCheckBox4,jCheckBox5,jCheckBox6,jCheckBox7};
        String checkOption="";
        int flag=0;
        for(int i=0;i<checkBox.length;i++){ //Perform looping for Day Off Check Box
            if(checkBox[i].isSelected()){
                flag++;
            }
        }
        if(flag<=2){
            flag=0;
          for(int i=0;i<checkBox.length;i++){
            if(checkBox[i].isSelected()){
                if(flag==0){
                     checkOption=checkBox[i].getText();
                   requestDayOff.add(checkBox[i].getText());
                  
                }else{
                    checkOption=checkOption+" , "+checkBox[i].getText();
               requestDayOff.add(checkBox[i].getText());
               
                }   
             flag++;
            }
        }  
          if(checkOption.equals("")){
            checkOption=" No day off";
        }
          int choice = JOptionPane.showOptionDialog(null, //Component parentComponent
                               "Name : "+jTextField1.getText()+"\nDay Request Off: "+checkOption+"\nPosition: "+
                                       jComboBox1.getSelectedItem().toString()+"\nTask: "+jTextField2.getText()+
                                       "\nStatus: "+jComboBox2.getSelectedItem().toString()+
                                      "", //Object message,
                               "Confirmation", //String title
                               JOptionPane.YES_NO_OPTION, //int optionType
                               JOptionPane.INFORMATION_MESSAGE, //int messageType
                               null, //Icon icon,
                              options,
                                options[1]); 
    if(choice == 0 ){
   updateStaff.setStaffName(jTextField1.getText());
   updateStaff.setStaffRequest(requestDayOff);
   updateStaff.setStaffPosition(jComboBox2.getSelectedItem().toString());
   updateStaff.setStaffTask(jTextField2.getText());
   updateStaff.setStaffStatus(jComboBox3.getSelectedItem().toString());
   staticStaffList.replace(updateNodePosition, updateStaff);
   JOptionPane.showMessageDialog(this,
    "Updated",
    "Status of the staff update",
    JOptionPane.INFORMATION_MESSAGE);
   updateStaff recallScreen=new updateStaff();
        recallScreen.setVisible(true);
        this.dispose();
        
        }else{
        //User choose to edit the details, perform ignore here
    }
    
    
        }else{
             JOptionPane.showMessageDialog(this,
        "Working day cannot less than 5",
        "Working day insufficient",
        JOptionPane.WARNING_MESSAGE);
         for(int i=0;i<checkBox.length;i++){ //Perform looping for Day Off Set False Due to Insufficient Day
            checkBox[i].setSelected(false);
        }
        }
        
                 
        
    }//GEN-LAST:event_jButton1ActionPerformed

